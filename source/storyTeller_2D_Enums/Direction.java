package storyTeller_2D_Enums;

/**
* Der Enum 'Direction' verwaltet alle Bewegungen des Spielers.
*/

public enum Direction {

/**
* Der Spieler geht nach rechts.
*/
	
	RIGHT,
	
/**
* Der Spieler geht nach links.	
*/
	
	LEFT,
	
/**
* Der Spieler geht vorwärts.	
*/
	
	FORWARD,
	
/**
* Der Spieler geht rückwärts.	
*/
	BACKWARDS;
	
}
